package composicao;

public class Departamento {
	
	private String nome;

	
	public Departamento() {
	}

	public Departamento(String nome) {
		this.nome = nome;
	}

	protected String getNome() {
		return nome;
	}

	protected void setNome(String nome) {
		this.nome = nome;
	} 
	
	
	
	

}
